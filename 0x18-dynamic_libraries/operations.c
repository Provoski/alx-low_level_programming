#include <stdio.h>

/**
 * add - add twp random numbers together
 * @a: variable for first number
 * @b: variable for second number
 * Return: addition of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: variable for first number
 * @b: variable for second number
 * Return: subtraction of b from a
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplie two numbers
 * @a: variable for first number
 * @b: variable for second number
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div a number
 * @a: variable for first number
 * @b: variable for second number
 * Return: division of a by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - find the quotient of two numbers
 * @a: variable for first number
 * @b: variable for second number
 * Return: quotient of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
